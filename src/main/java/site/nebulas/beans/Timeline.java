package site.nebulas.beans;

public class Timeline {
	private Integer timelineId;
	private String timelineCaption;
	private String timelineContent;
	private String timelineAddTime;
	
	
	public Integer getTimelineId() {
		return timelineId;
	}
	public void setTimelineId(Integer timelineId) {
		this.timelineId = timelineId;
	}
	public String getTimelineCaption() {
		return timelineCaption;
	}
	public void setTimelineCaption(String timelineCaption) {
		this.timelineCaption = timelineCaption;
	}
	public String getTimelineContent() {
		return timelineContent;
	}
	public void setTimelineContent(String timelineContent) {
		this.timelineContent = timelineContent;
	}
	public String getTimelineAddTime() {
		return timelineAddTime;
	}
	public void setTimelineAddTime(String timelineAddTime) {
		this.timelineAddTime = timelineAddTime;
	}
	
	
}
